CREATE TABLE IF NOT EXISTS public.teams (
  id bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  name text NOT NULL,
  primary_kit_color text NOT NULL,
  away_kit_color text NOT NULL,
  invite_code text UNIQUE,
  created_by bigint
);

CREATE TABLE IF NOT EXISTS public.users (
  id bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  auth_id uuid UNIQUE REFERENCES auth.users(id) ON DELETE CASCADE,
  name text NOT NULL,
  email text NOT NULL UNIQUE,
  role public.user_role NOT NULL DEFAULT 'Player',
  team_id bigint REFERENCES public.teams(id)
);

DO $$ BEGIN
  ALTER TABLE public.teams
    ADD CONSTRAINT teams_created_by_fkey
    FOREIGN KEY (created_by) REFERENCES public.users(id) ON DELETE SET NULL;
EXCEPTION WHEN duplicate_object THEN NULL;
END $$;

CREATE INDEX IF NOT EXISTS idx_teams_invite_code ON public.teams(invite_code);
CREATE INDEX IF NOT EXISTS idx_users_auth_id ON public.users(auth_id);

CREATE TABLE IF NOT EXISTS public.matches (
  id bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  home_team_id bigint NOT NULL REFERENCES public.teams(id),
  away_team_id bigint NOT NULL REFERENCES public.teams(id),
  match_date timestamptz NOT NULL,
  field_number integer NOT NULL DEFAULT 1,
  field_address text,
  status public.match_status NOT NULL DEFAULT 'Scheduled',
  assigned_home_kit text NOT NULL,
  assigned_away_kit text NOT NULL,
  home_score integer,
  away_score integer
);

CREATE TABLE IF NOT EXISTS public.rsvps (
  id bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  match_id bigint NOT NULL REFERENCES public.matches(id) ON DELETE CASCADE,
  user_id bigint NOT NULL REFERENCES public.users(id) ON DELETE CASCADE,
  status public.rsvp_status NOT NULL DEFAULT 'No Response',
  UNIQUE (match_id, user_id)
);

CREATE TABLE IF NOT EXISTS public.messages (
  id bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  text text NOT NULL,
  sender_name text NOT NULL,
  "timestamp" timestamptz NOT NULL DEFAULT now(),
  team_id bigint REFERENCES public.teams(id) ON DELETE CASCADE
);

CREATE INDEX IF NOT EXISTS idx_messages_team_id ON public.messages(team_id);

CREATE TABLE IF NOT EXISTS public.match_goals (
  id bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  match_id bigint NOT NULL REFERENCES public.matches(id) ON DELETE CASCADE,
  team_id bigint NOT NULL REFERENCES public.teams(id),
  scorer_id bigint NOT NULL REFERENCES public.users(id),
  assist_id bigint REFERENCES public.users(id),
  created_at timestamptz NOT NULL DEFAULT now()
);

CREATE INDEX IF NOT EXISTS idx_match_goals_match_id ON public.match_goals(match_id);

