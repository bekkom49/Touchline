CREATE OR REPLACE FUNCTION public.auth_user_id()
RETURNS bigint
LANGUAGE sql STABLE SECURITY DEFINER SET search_path = public
AS $$ SELECT id FROM public.users WHERE auth_id = auth.uid() LIMIT 1; $$;

CREATE OR REPLACE FUNCTION public.auth_user_role()
RETURNS public.user_role
LANGUAGE sql STABLE SECURITY DEFINER SET search_path = public
AS $$ SELECT role FROM public.users WHERE auth_id = auth.uid() LIMIT 1; $$;

CREATE OR REPLACE FUNCTION public.auth_user_team_id()
RETURNS bigint
LANGUAGE sql STABLE SECURITY DEFINER SET search_path = public
AS $$ SELECT team_id FROM public.users WHERE auth_id = auth.uid() LIMIT 1; $$;

CREATE OR REPLACE FUNCTION public.auth_user_name()
RETURNS text
LANGUAGE sql STABLE SECURITY DEFINER SET search_path = public
AS $$ SELECT name FROM public.users WHERE auth_id = auth.uid() LIMIT 1; $$;

CREATE OR REPLACE FUNCTION public.is_organizer()
RETURNS boolean
LANGUAGE sql STABLE SECURITY DEFINER SET search_path = public
AS $$ SELECT EXISTS (SELECT 1 FROM public.users WHERE auth_id = auth.uid() AND role = 'Organizer'); $$;

CREATE OR REPLACE FUNCTION public.is_captain()
RETURNS boolean
LANGUAGE sql STABLE SECURITY DEFINER SET search_path = public
AS $$ SELECT EXISTS (SELECT 1 FROM public.users WHERE auth_id = auth.uid() AND role = 'Captain'); $$;

CREATE OR REPLACE FUNCTION public.is_player()
RETURNS boolean
LANGUAGE sql STABLE SECURITY DEFINER SET search_path = public
AS $$ SELECT EXISTS (SELECT 1 FROM public.users WHERE auth_id = auth.uid() AND role = 'Player'); $$;

