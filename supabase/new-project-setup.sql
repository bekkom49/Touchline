CREATE POLICY "teams_select_public"
  ON public.teams FOR SELECT TO anon, authenticated USING (true);

CREATE POLICY "teams_insert_captain"
  ON public.teams FOR INSERT TO authenticated
  WITH CHECK (public.is_captain());

CREATE POLICY "users_select_authenticated"
  ON public.users FOR SELECT TO authenticated
  USING (
    auth_id = auth.uid()
    OR public.is_organizer()
    OR (public.auth_user_team_id() IS NOT NULL AND team_id = public.auth_user_team_id())
  );

CREATE POLICY "users_insert_own_profile"
  ON public.users FOR INSERT TO authenticated
  WITH CHECK (auth_id = auth.uid() AND email = (auth.jwt() ->> 'email'));

CREATE POLICY "users_insert_captain_roster"
  ON public.users FOR INSERT TO authenticated
  WITH CHECK (
    public.is_captain()
    AND role = 'Player'
    AND team_id = public.auth_user_team_id()
    AND auth_id IS NULL
  );

CREATE POLICY "users_update_link_auth"
  ON public.users FOR UPDATE TO authenticated
  USING (email = (auth.jwt() ->> 'email') AND auth_id IS NULL)
  WITH CHECK (auth_id = auth.uid() AND email = (auth.jwt() ->> 'email'));

CREATE POLICY "users_update_own_profile"
  ON public.users FOR UPDATE TO authenticated
  USING (auth_id = auth.uid())
  WITH CHECK (auth_id = auth.uid());

CREATE POLICY "users_delete_captain_roster"
  ON public.users FOR DELETE TO authenticated
  USING (
    public.is_captain()
    AND role = 'Player'
    AND team_id = public.auth_user_team_id()
    AND auth_id IS NULL
    AND id <> public.auth_user_id()
  );

CREATE POLICY "matches_select_authenticated"
  ON public.matches FOR SELECT TO authenticated USING (true);

CREATE POLICY "matches_insert_organizer"
  ON public.matches FOR INSERT TO authenticated
  WITH CHECK (public.is_organizer());

CREATE POLICY "matches_update_organizer"
  ON public.matches FOR UPDATE TO authenticated
  USING (public.is_organizer())
  WITH CHECK (public.is_organizer());

CREATE POLICY "rsvps_select_scoped"
  ON public.rsvps FOR SELECT TO authenticated
  USING (
    public.is_organizer()
    OR user_id = public.auth_user_id()
    OR (
      public.auth_user_team_id() IS NOT NULL
      AND EXISTS (
        SELECT 1 FROM public.users u
        WHERE u.id = rsvps.user_id AND u.team_id = public.auth_user_team_id()
      )
    )
  );

CREATE POLICY "rsvps_insert_own"
  ON public.rsvps FOR INSERT TO authenticated
  WITH CHECK (
    public.is_player()
    AND user_id = public.auth_user_id()
    AND status IN ('Going', 'Out', 'No Response')
    AND EXISTS (
      SELECT 1 FROM public.matches m
      WHERE m.id = match_id AND m.status = 'Scheduled'
        AND public.auth_user_team_id() IS NOT NULL
        AND (m.home_team_id = public.auth_user_team_id() OR m.away_team_id = public.auth_user_team_id())
    )
  );

CREATE POLICY "rsvps_update_own"
  ON public.rsvps FOR UPDATE TO authenticated
  USING (public.is_player() AND user_id = public.auth_user_id())
  WITH CHECK (
    public.is_player()
    AND user_id = public.auth_user_id()
    AND status IN ('Going', 'Out', 'No Response')
    AND EXISTS (
      SELECT 1 FROM public.matches m
      WHERE m.id = match_id AND m.status = 'Scheduled'
        AND public.auth_user_team_id() IS NOT NULL
        AND (m.home_team_id = public.auth_user_team_id() OR m.away_team_id = public.auth_user_team_id())
    )
  );

CREATE POLICY "messages_select_scoped"
  ON public.messages FOR SELECT TO authenticated
  USING (
    team_id IS NULL
    OR public.is_organizer()
    OR team_id = public.auth_user_team_id()
  );

CREATE POLICY "messages_insert_as_self"
  ON public.messages FOR INSERT TO authenticated
  WITH CHECK (
    sender_name = public.auth_user_name()
    AND char_length(trim(text)) > 0
    AND (team_id IS NULL OR team_id = public.auth_user_team_id())
  );

CREATE POLICY "messages_insert_league_office"
  ON public.messages FOR INSERT TO authenticated
  WITH CHECK (
    public.is_organizer()
    AND sender_name = 'League Office'
    AND team_id IS NULL
    AND char_length(trim(text)) > 0
  );

